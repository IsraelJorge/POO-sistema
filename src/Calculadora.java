public class Calculadora {
    public int soma(int v1, int v2) {
        return v1 + v2;
    }
    
    public int sub(int v1, int v2) {
        return v1 - v2;
    }
    
    public int mult(int v1, int v2) {
        return v1 * v2;
    }

    public double div(double v1, double v2) {
        return v1 / v2;
    }
}